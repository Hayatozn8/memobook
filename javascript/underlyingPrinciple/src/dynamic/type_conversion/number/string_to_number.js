// string ---> number 测试

// 1. 直接输出 8 进制数值字面量
var a = 012;
console.log(a); // 10

// 2. 尝试将 8 进制数值字符串转换为数字
var b = '012';
console.log(+b);    // 12
// 无法转换，会被当作 10 进制数处理

// 3. 处理包含小数的 10 进制数值字符串
// 开头的多个 0 将会被省略
var c = '00123.45';
console.log(+c);    // 123.45

// 4. 处理 16 进制数值字符串
var d = '0x1234';
console.log(+d);    // 4660

// 5. 处理包含指数标识的数值字符串
// JS会将指数标识展开成普通数值
var e = '1.23e4';
console.log(+e);    // 12300